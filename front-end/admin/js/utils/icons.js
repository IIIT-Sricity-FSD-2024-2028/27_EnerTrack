export function getIcon(name) {
  return icons[name] || '';
}

export function injectIcons() {
  document.querySelectorAll('[data-icon]').forEach(el => {
    const iconName = el.dataset.icon;
    const svgContent = getIcon(iconName);
    if (svgContent) {
      el.innerHTML = svgContent;
    }
  });
}
